    private void SignUpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SignUpActionPerformed
        if( !(checkName && checkEmail && checkContact && checkPassword)){
            JOptionPane.showMessageDialog(this, "Incorrect Email or Password");
        }
        else{
        try{
            Connection con = ConnectionProvider.getCon();
            Statement p = con.createStatement();
            
            // result set for generating new student Id
            ResultSet r = p.executeQuery("Select count(*) from Student ");
            if(r.next())
               sId = r.getInt(1)+1;
            
            // checking student already Enrolled or not
            ResultSet rs = p.executeQuery("Select * from Student where email='"+email+"'");
            if(rs.next()){
                JOptionPane.showMessageDialog(this,"this Student is already enrolled");
            }
            else{
                // adding new student into table
                PreparedStatement ps = con.prepareStatement("Insert into student values(?,?,?,?,?)");
                ps.setInt(1, sId);
                ps.setString(2, name);
                ps.setString(3, email);
                ps.setLong(4, Long.parseLong(contact));
                ps.setString(5, password);
                ps.executeUpdate();
                JOptionPane.showMessageDialog(this,"student registered Succesfully");
                setVisible(false);
                new chooseTopic(sId).setVisible(true);
            }
            
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(this,e);
        }
        }
    }//GEN-LAST:event_SignUpActionPerformed

    private void EmailFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_EmailFocusLost
        email =Email.getText().trim();
        if( email.equals("")){
           emailWarning.setText("required ");
           Email.setText("Email-address");
           Email.setForeground(new Color(204,204,204));
       }
       else if(!email.endsWith("@gmail.com")){
           emailWarning.setText("Invalid EmailId ");
       }
       else
           checkEmail=true;
    }//GEN-LAST:event_EmailFocusLost

