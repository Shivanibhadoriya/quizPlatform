    private void ContactNumFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_ContactNumFocusLost
        contact = ContactNum.getText().trim();
        if( contact.equals("")){
           ContactWarning.setText("required ");
           ContactNum.setText("Mobile Number");
           ContactNum.setForeground(new Color(204,204,204));
       }
       else if(contact.length() != 10){
            ContactWarning.setText("Invalid Contact Number");
        }else{
            checkContact=true;
            for(char ch:contact.toCharArray()){
                if(!Character.isDigit(ch)){
                    ContactWarning.setText("Invalid Contact Number");
                    checkContact = false;
                    break;
                }
            }
        }
    }//GEN-LAST:event_ContactNumFocusLost

