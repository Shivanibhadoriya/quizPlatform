    private void UpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateActionPerformed
             String id = ID.getText();
           String Name = name.getText();
           String opt1 = option1.getText();
           String opt2 = option2.getText();
           String opt3 = option3.getText();
           String opt4 = option4.getText();
           String ans = answer.getText();
           try{
              Connection con= ConnectionProvider.getCon();
               PreparedStatement PS = con.prepareStatement("Update question set name=? ,op1=? , op2=?,op3=?,op4=?,answer=? where id=?");
               PS.setString(1,Name);
               PS.setString(2,opt1);
               PS.setString(3,opt2);
               PS.setString(4,opt3);
               PS.setString(5,opt4);
               PS.setString(6,ans);
               PS.setString(7,id);
               
               PS.executeUpdate();
               JFrame jf= new JFrame();
               jf.setAlwaysOnTop(true);
               JOptionPane.showMessageDialog(jf,"Successfully Updated");
               setVisible(false);
               new updateQuestion().setVisible(true); 
           }
           catch(Exception e){
                JFrame jf= new JFrame();
                jf.setAlwaysOnTop(true);
                JOptionPane.showMessageDialog(jf,e);
           
           }
    }//GEN-LAST:event_UpdateActionPerformed

